/** หา post จาก slug */
function getPostBySlug(slug) {
  return BLOG_POSTS.find(p => p.slug === slug) || null;
}

/** กรอง posts ตาม category (null = ทั้งหมด) */
function getPostsByCategory(category) {
  if (!category || category === 'all') return BLOG_POSTS;
  return BLOG_POSTS.filter(p => p.category === category);
}

/** ค้นหา posts จาก keyword */
function searchPosts(keyword) {
  const q = keyword.toLowerCase().trim();
  if (!q) return BLOG_POSTS;
  return BLOG_POSTS.filter(p =>
    p.title.toLowerCase().includes(q) ||
    p.excerpt.toLowerCase().includes(q) ||
    p.tags.some(t => t.toLowerCase().includes(q)) ||
    p.categoryLabel.includes(q)
  );
}

/** หา related posts (same category, exclude current) */
function getRelatedPosts(currentId, limit = 3) {
  const current = BLOG_POSTS.find(p => p.id === currentId);
  if (!current) return [];
  return BLOG_POSTS
    .filter(p => p.id !== currentId && p.category === current.category)
    .slice(0, limit);
}

/** format date label */
function formatDate(dateStr) {
  const d = new Date(dateStr);
  const months = ['ม.ค.','ก.พ.','มี.ค.','เม.ย.','พ.ค.','มิ.ย.','ก.ค.','ส.ค.','ก.ย.','ต.ค.','พ.ย.','ธ.ค.'];
  return `${d.getDate()} ${months[d.getMonth()]} ${d.getFullYear() + 543}`;
}
